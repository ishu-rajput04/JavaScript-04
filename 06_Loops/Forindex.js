// for loop


for (let i = 1; i <= 5; i++) {
    console.log("hello");

}

//---------------> Reverse counting

let i;
for (let i = 10; i >= 1; i--) {
    console.log(i);
}


//------------- break keyword---------

for (let a = 1; a <= 5; a++) {
    if (a == 4)
        break;
    else
        console.log(a);
}